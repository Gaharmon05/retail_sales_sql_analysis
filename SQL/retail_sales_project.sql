SELECT
	*
FROM
	CUSTOMERS;

SELECT
	*
FROM
	PRODUCTS;

SELECT
	*
FROM
	ORDERS;

SELECT
	P.PRODUCT_NAME,
	SUM(O.QUANTITY * P.PRICE) AS TOTAL_REVENUE
FROM
	ORDERS O
	JOIN PRODUCTS P ON O.PRODUCT_ID = P.PRODUCT_ID
GROUP BY
	P.PRODUCT_NAME
ORDER BY
	TOTAL_REVENUE DESC;

SELECT
	C.FIRST_NAME,
	C.LAST_NAME,
	SUM(O.QUANTITY * P.PRICE) AS TOTAL_SPENT
FROM
	ORDERS O
	JOIN CUSTOMERS C ON O.CUSTOMER_ID = C.CUSTOMER_ID
	JOIN PRODUCTS P ON O.PRODUCT_ID = P.PRODUCT_ID
GROUP BY
	C.FIRST_NAME,
	C.LAST_NAME
ORDER BY
	TOTAL_SPENT DESC;

SELECT
	DATE_TRUNC('month', O.ORDER_DATE) AS SALES_MONTH,
	SUM(O.QUANTITY * P.PRICE) AS MONTHLY_REVENUE
FROM
	ORDERS O
	JOIN PRODUCTS P ON O.PRODUCT_ID = P.PRODUCT_ID
GROUP BY
	SALES_MONTH
ORDER BY
	SALES_MONTH;

SELECT
	P.CATEGORY,
	SUM(O.QUANTITY * P.PRICE) AS CATEGORY_REVENUE
FROM
	ORDERS O
	JOIN PRODUCTS P ON O.PRODUCT_ID = P.PRODUCT_ID
GROUP BY
	P.CATEGORY
ORDER BY
	CATEGORY_REVENUE DESC;